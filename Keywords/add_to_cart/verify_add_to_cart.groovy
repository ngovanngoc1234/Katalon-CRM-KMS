class verify_add_to_cart {
	/**
	 * Refresh browser
	 */
	@Keyword
	def refreshBrowser() {
		KeywordUtil.logInfo("Refreshing")
		WebDriver webDriver = DriverFactory.getWebDriver()
		webDriver.navigate().refresh()
		KeywordUtil.markPassed("Refresh successfully")
	}

	/**
	 * Click element
	 * @param to Katalon test object
	 */
	@Keyword
	def clickElement(TestObject to) {
		try {
			WebElement element = WebUiBuiltInKeywords.findWebElement(to);
			KeywordUtil.logInfo("Clicking element")
			element.click()
			KeywordUtil.markPassed("Element has been clicked")
		} catch (WebElementNotFoundException e) {
			KeywordUtil.markFailed("Element not found")
		} catch (Exception e) {
			KeywordUtil.markFailed("Fail to click on element")
		}
	}

	/**
	 * Get all rows of HTML table
	 * @param table Katalon test object represent for HTML table
	 * @param outerTagName outer tag name of TR tag, usually is TBODY
	 * @return All rows inside HTML table
	 */
	@Keyword
	def List<WebElement> getHtmlTableRows(TestObject table, String outerTagName) {
		WebElement mailList = WebUiBuiltInKeywords.findWebElement(table)
		List<WebElement> selectedRows = mailList.findElements(By.xpath("./" + outerTagName + "/tr"))
		return selectedRows
	}
	/**
	 * Verify item when add to cart
	 * @param locator_count_cartitems
	 */
	@Keyword
	def verifyItemAddToCart(String locator_count_cartitems) {

		WebDriver driver = DriverFactory.getWebDriver()

		List<String> element = driver.findElements(By.xpath(locator_count_cartitems))

		int total_element = element.size()

		'Get title item which user added to cart'
		String actual_result = WebUI.getText(findTestObject('Women tab/txt_item'))

		'Click add one item add to cart'
		WebUI.click(findTestObject('Women tab/btn_add_to_cart'))

		'Count number item to cart'
		String count_cart_string = WebUI.getText(findTestObject('Women tab/lbl_cout_cart'))

		int count_cart_number = count_cart_string.toInteger()

		'Verify count number when click only one item '
		WebUI.verifyEqual(count_cart_number, count_cart_number)

		'Click button shopping cart'
		WebUI.click(findTestObject('Women tab/href_cart'))

		'Get title item into cart of one item'
		String expected_result = WebUI.getText(findTestObject('Women tab/txt_item_cart'))

		'Verify title at home page women compare with tile into cart'
		WebUI.verifyMatch(actual_result, expected_result, false)
	}
}