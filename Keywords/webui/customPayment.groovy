class customPayment {
	/**
	 * Refresh browser
	 */
	@Keyword
	def refreshBrowser() {
		KeywordUtil.logInfo("Refreshing")
		WebDriver webDriver = DriverFactory.getWebDriver()
		webDriver.navigate().refresh()
		KeywordUtil.markPassed("Refresh successfully")
	}


	/**
	 * Payment
	 * @param email
	 * @param cardnumber
	 * @param expdate
	 * @param cvc
	 * @param zip
	 * @return
	 */
	@Keyword
	def paymentWithCard(String email, String cardnumber, String expdate, String cvc, String zip) {

		KeywordUtil.logInfo("Paymencard")

		KeywordUtil.logInfo("Enter email")
		WebUI.setText(findTestObject('Cart Page/txt_email'), email)

		KeywordUtil.logInfo("Switch to iFrame")
		WebUI.switchToFrame(findTestObject('Cart Page/iframe_cardNumber'), 30)

		KeywordUtil.logInfo("Enter card number")
		WebUI.setText(findTestObject('Cart Page/txt_cardNumber'), cardnumber)

		KeywordUtil.logInfo("Enter expire date")
		WebUI.setText(findTestObject('Cart Page/txt_date'), expdate)

		KeywordUtil.logInfo("Enter CVC/CVV")
		WebUI.setText(findTestObject('Cart Page/txt_cvcNumber'), cvc)

		KeywordUtil.logInfo("Enter postal")
		WebUI.setText(findTestObject('Cart Page/txt_zip'), zip)

		KeywordUtil.logInfo("Switch to default content")
		WebUI.switchToDefaultContent()

		KeywordUtil.logInfo("Click on Pay with credit card")
		WebUI.click(findTestObject('Cart Page/btn_payment'))

		KeywordUtil.markPassed("Refresh successfully")
	}
	/**
	 * Click element
	 * @param to Katalon test object
	 */
	@Keyword
	def clickElement(TestObject to) {
		try {
			WebElement element = WebUiBuiltInKeywords.findWebElement(to);
			KeywordUtil.logInfo("Clicking element")
			element.click()
			KeywordUtil.markPassed("Element has been clicked")
		} catch (WebElementNotFoundException e) {
			KeywordUtil.markFailed("Element not found")
		} catch (Exception e) {
			KeywordUtil.markFailed("Fail to click on element")
		}
	}

	/**
	 * Get all rows of HTML table
	 * @param table Katalon test object represent for HTML table
	 * @param outerTagName outer tag name of TR tag, usually is TBODY
	 * @return All rows inside HTML table
	 */
	@Keyword
	def List<WebElement> getHtmlTableRows(TestObject table, String outerTagName) {
		WebElement mailList = WebUiBuiltInKeywords.findWebElement(table)
		List<WebElement> selectedRows = mailList.findElements(By.xpath("./" + outerTagName + "/tr"))
		return selectedRows
	}
}